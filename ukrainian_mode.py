_WORD_BOUNDARY_RE = re.compile(r'(\b\w+\b)([^\w]*)')

_SORTED_KEYS = sorted(UKRAINIAN_WORD_REPLACEMENTS.keys(), key=len, reverse=True)
_COMPILED_DICT = {k: re.compile(r'\b' + re.escape(k) + r'\b', re.IGNORECASE) for k in _SORTED_KEYS}


def _get_replacement(key):
    val = UKRAINIAN_WORD_REPLACEMENTS[key]
    if isinstance(val, list):
        return random.choice(val)
    return val


def _match_case(original: str, replacement: str) -> str:
    if original.isupper():
        return replacement.upper()
    if original.istitle():
        return replacement.capitalize()
    if original.islower():
        return replacement.lower()
    return replacement


def _stage1_dict_replace(text: str) -> tuple[str, set]:
    replaced_spans = set()
    result = text

    for key in _SORTED_KEYS:
        pattern = _COMPILED_DICT[key]
        matches = list(pattern.finditer(result))
        if not matches:
            continue
        offset = 0
        for m in matches:
            start = m.start() + offset
            end = m.end() + offset
            original = m.group(0)
            replacement = _get_replacement(key)
            replacement = _match_case(original, replacement)
            result = result[:start] + replacement + result[end:]
            diff = len(replacement) - len(original)
            offset += diff
            for i in range(start, start + len(replacement)):
                replaced_spans.add(i)

    return result, replaced_spans


def _replace_char(chars: list[str], i: int) -> None:
    c = chars[i]
    cl = c.lower()

    if cl == 'ы':
        chars[i] = 'и' if c.islower() else 'И'
    elif cl == 'э':
        chars[i] = 'є' if c.islower() else 'Є'
    elif cl == 'и':
        if i > 0 and chars[i - 1].lower() in 'аеёиоуыэюяієї':
            if random.random() < 0.6:
                chars[i] = 'ї' if c.islower() else 'Ї'
        else:
            if random.random() < 0.5:
                chars[i] = 'i' if c.islower() else 'I'
    elif cl == 'е':
        if i == 0 or not chars[i - 1].isalpha():
            if random.random() < 0.4:
                chars[i] = 'є' if c.islower() else 'Є'
    elif cl == 'г':
        if random.random() < 0.10:
            chars[i] = 'ґ' if c.islower() else 'Ґ'

def _process_triplet_tsya(chars: list[str], i: int, replaced_spans: set) -> None:
    if i >= 2 and i < len(chars):
        triplet = ''.join(chars[i - 2:i + 1]).lower()
        if triplet == 'тся' and (i - 2) not in replaced_spans:
            chars[i - 1] = 'ь' if chars[i - 1].islower() else 'Ь'
            chars.insert(i, 'с' if chars[i].islower() else 'С')

def _stage2_linguistic(text: str, replaced_spans: set) -> str:
    chars = list(text)
    i = 0
    while i < len(chars):
        if i in replaced_spans:
            i += 1
            continue

        _replace_char(chars, i)
        _process_triplet_tsya(chars, i, replaced_spans)

        i += 1

    return ''.join(chars)


