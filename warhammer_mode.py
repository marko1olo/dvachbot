# 1. Создаем единое регулярное выражение для всех слов, которые нужно заменить.
# Сортируем ключи по длине в обратном порядке, чтобы избежать частичных совпадений.
_wh40k_sorted_keys = sorted(WH40K_REPLACEMENTS.keys(), key=len, reverse=True)
_wh40k_pattern = r'\b(' + '|'.join(re.escape(key) for key in _wh40k_sorted_keys) + r')\b'
_wh40k_regex = re.compile(_wh40k_pattern, flags=re.IGNORECASE)

# 2. Создаем функцию-заменитель, которая будет вызываться для каждого совпадения.
def _wh40k_replacer(match: re.Match) -> str:
    """
    Находит замену для совпавшего слова, сохраняет его регистр и выбирает случайный вариант.
    """
    original_word = match.group(0)
    replacement_options = WH40K_REPLACEMENTS[original_word.lower()]

    if isinstance(replacement_options, list):
        chosen_replacement = random.choice(replacement_options)
    else:
        chosen_replacement = replacement_options

    if original_word.isupper():
        return chosen_replacement.upper()
    elif original_word.istitle():
        return chosen_replacement.capitalize()
    else:
        return chosen_replacement
def _apply_high_gothic_and_binaric(text: str) -> str:
    # 1. High Gothic (Псевдо-Латынь)
    def _latinize(m):
        word = m.group(0)
        if random.random() < 0.18:
            if word[-1].lower() in 'аяиы':
                return word[:-1] + ("um" if word.islower() else "UM")
            elif word[-1].lower() in 'ьй':
                return word[:-1] + ("us" if word.islower() else "US")
            elif word[-1].lower() in 'ое':
                return word[:-1] + ("atus" if word.islower() else "ATUS")
            else:
                return word + ("ius" if word.islower() else "IUS")
        return word

    text = re.sub(r'\b[А-Яа-яЁёa-zA-Z]{6,}\b', _latinize, text)
    
    # 2. Binaric cant (Священный бинарный код Адептус Механикус)
    def _insert_binaric(m):
        word = m.group(0)
        if random.random() < 0.08:
            binary_burst = "".join(random.choice(['0', '1']) for _ in range(8))
            return f"{word} [{binary_burst}]"
        return word
        
    text = re.sub(r'\b[А-Яа-яЁёa-zA-Z]+\b', _insert_binaric, text)
    
    return text
