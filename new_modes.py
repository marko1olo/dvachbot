from __future__ import annotations

import random
import re
from collections.abc import Mapping, Sequence


ReplacementValue = str | Sequence[str]


def _key(text: str) -> str:
    return text.casefold().replace("ё", "е")


def _choose(value: ReplacementValue) -> str:
    if isinstance(value, str):
        return value
    return random.choice(value)


def _match_case(source: str, replacement: str) -> str:
    if source.isupper():
        return replacement.upper()
    if source[:1].isupper():
        return replacement[:1].upper() + replacement[1:]
    return replacement


def _build_pattern(replacements: Mapping[str, ReplacementValue]) -> re.Pattern[str]:
    words = sorted((re.escape(word) for word in replacements), key=len, reverse=True)
    return re.compile(r"(?iu)(?<![\w])(" + "|".join(words) + r")(?![\w])")


def _swap_words(text: str, replacements: Mapping[str, ReplacementValue], chance: float, pattern: re.Pattern[str]) -> str:
    def repl(match: re.Match[str]) -> str:
        source = match.group(0)
        if random.random() > chance:
            return source
        value = replacements.get(_key(source))
        if value is None:
            return source
        return _match_case(source, _choose(value))

    return pattern.sub(repl, text)


def _inject_words(text: str, injections: Sequence[str], chance: float, max_items: int) -> str:
    if len(text) > 900:
        return text
    words = text.split()
    if len(words) < 5 or random.random() > chance:
        return text
    count = min(max_items, max(1, len(words) // 16))
    for _ in range(random.randint(1, count)):
        idx = random.randrange(1, len(words))
        words.insert(idx, random.choice(injections))
    return " ".join(words)


def _decorate(text: str, profile: dict) -> str:
    if not text.strip():
        return text
    transformed = _swap_words(
        text,
        profile["replacements"],
        profile.get("replace_chance", 0.45),
        profile["pattern"],
    )
    transformed = _inject_words(
        transformed,
        profile["injections"],
        profile.get("inject_chance", 0.35),
        profile.get("max_injections", 2),
    )
    if len(transformed) < 900 and random.random() < profile.get("prefix_chance", 0.38):
        transformed = f"{random.choice(profile['prefixes'])} {transformed}"
    if len(transformed) < 900 and random.random() < profile.get("suffix_chance", 0.38):
        transformed = f"{transformed} {random.choice(profile['suffixes'])}"
    if len(transformed) > 3900:
        transformed = transformed[:3890].rstrip() + "..."
    return transformed


