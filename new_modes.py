def _apply_holiday_effects(text: str) -> str:
    # 1. Drunk Typos (30% chance)
    if random.random() < 0.30:
        chars = list(text)
        layout = {
            'й':'ц','ц':'у','у':'к','к':'е','е':'н','н':'г','г':'ш','ш':'щ','щ':'з','з':'х','х':'ъ',
            'ф':'ы','ы':'в','в':'а','а':'п','п':'р','р':'о','о':'л','л':'д','д':'ж','ж':'э',
            'я':'ч','ч':'с','с':'м','м':'и','и':'т','т':'ь','ь':'б','б':'ю'
        }
        rev_layout = {v: k for k, v in layout.items()}
        all_layout = {**layout, **rev_layout}
        
        for i in range(len(chars)):
            c = chars[i].lower()
            if c in all_layout and random.random() < 0.08:
                replaced = all_layout[c]
                chars[i] = replaced.upper() if chars[i].isupper() else replaced
        text = "".join(chars)

    # 2. Vowel stretching
    vowels = "аеиоуыэюяёАЕИОУЫЭЮЯЁ"
    words = text.split()
    for i in range(len(words)):
        if len(words[i]) > 3 and random.random() < 0.15:
            v_indices = [idx for idx, char in enumerate(words[i]) if char in vowels]
            if v_indices:
                v_idx = random.choice(v_indices)
                char = words[i][v_idx]
                words[i] = words[i][:v_idx] + char * random.randint(2, 4) + words[i][v_idx+1:]
    text = " ".join(words)

    # 3. Drunken sounds injection
    if random.random() < 0.25:
        sounds = ["*ик*", "*бульк*", "*упал под стол*", "(лицом в оливье)", "(разбил рюмку)"]
        words = text.split()
        if words:
            idx = random.randint(0, len(words))
            words.insert(idx, random.choice(sounds))
            text = " ".join(words)

    # 4. Drunken Shouting (UPPERCASE)
    words = text.split()
    for i in range(len(words)):
        if random.random() < 0.15:
            words[i] = words[i].upper()
    text = " ".join(words)

    # 5. Toasts
    if random.random() < 0.30:
        toasts = [
            " «НУ, ЗА ЗДОРОВЬЕ АНОНА!»", " «ТАМАДА, НАЛИВАЙ!»", 
            " «ГОРЬКО!»", " «ПОЕХАЛИ!»", " «НУ, ЗА ПОНИМАНИЕ!»"
        ]
        text += random.choice(toasts)

    return text


