def _decorate_custom(text: str, profile: ModeProfile, custom_func) -> str:
    result = _replace_words(text, profile)
    result = custom_func(result)
    result = _inject(result, profile)
    if len(result) < 1400 and random.random() < profile.prefix_chance:
        result = f"{_choose(profile.prefixes)} {result}"
    if len(result) < 1400 and random.random() < profile.suffix_chance:
        result = f"{result} {_choose(profile.suffixes)}"
    if len(result) < 1600 and random.random() < profile.slogan_chance:
        result = f"{result}\n\n<b>{_choose(profile.slogans)}</b>"
    return result


