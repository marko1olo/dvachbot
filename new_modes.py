def _replace_words(text: str, profile: ModeProfile) -> str:
    if profile.pattern is None:
        return text

    used = set()

    def repl(match: re.Match[str]) -> str:
        source = match.group(0)
        options = profile.replacements.get(_key(source))
        if not options or random.random() > profile.replace_chance:
            return source
        available = [opt for opt in options if opt not in used]
        if not available:
            available = list(options)
        selected = _choose(available)
        used.add(selected)
        return _match_case(source, selected)

    return profile.pattern.sub(repl, text)


def _inject(text: str, profile: ModeProfile) -> str:
    if not text or len(text) > 1400 or random.random() > profile.inject_chance:
        return text
    if len(text.split()) < 5:
        return text
    max_items = min(profile.max_injections, 2)
    insert = " ".join(_choose(profile.injections) for _ in range(random.randint(1, max_items)))
    if random.random() < 0.35:
        return f"{insert} {text}"
    return f"{text} {insert}"


