def _extend_profile(
    profile: dict,
    prefixes: Sequence[str],
    suffixes: Sequence[str],
    injections: Sequence[str],
    replacements: Mapping[str, ReplacementValue],
) -> None:
    profile["prefixes"].extend(prefixes)
    profile["suffixes"].extend(suffixes)
    profile["injections"].extend(injections)
    profile["replacements"].update({_key(key): value for key, value in replacements.items()})
    profile["replace_chance"] = 0.62
    profile["inject_chance"] = 0.50
    profile["max_injections"] = 3


