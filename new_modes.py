def _expand_replacements(concepts: Mapping[str, Sequence[str]]) -> ReplacementMap:
    replacements: ReplacementMap = {}
    for concept, options in concepts.items():
        aliases = _CONCEPT_ALIASES.get(concept, (concept,))
        values = tuple(options)
        for alias in aliases:
            replacements[_key(alias)] = values
    return replacements


def _make_profile(name: str) -> ModeProfile:
    replacements = _expand_replacements(_CONCEPT_REPLACEMENTS[name])
    for phrase, values in _EXACT_REPLACEMENTS.get(name, {}).items():
        replacements[_key(phrase)] = tuple(values)
    return ModeProfile(
        prefixes=tuple(_PREFIXES[name]),
        suffixes=tuple(_SUFFIXES[name]),
        injections=tuple(_INJECTIONS[name]),
        slogans=tuple(_SLOGANS[name]),
        replacements=replacements,
        pattern=_build_pattern(replacements),
    )


