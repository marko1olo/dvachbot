def get_dynamic_proxy_url():
    """
    Сканирует порты Hiddify/V2Ray.
    Если порты закрыты — возвращает None (режим Direct/OpenVPN).
    """
    # 2334/12334 - Hiddify/Nekobox, 2080/1080 - V2Ray/Socks, 7890 - Clash
    PORTS = [2334, 12334, 2080, 1080, 7890]
    
    for port in PORTS:
        try:
            # Быстрый пинг порта (0.1с)
            with socket.create_connection(("127.0.0.1", port), timeout=0.1):
                return f"http://127.0.0.1:{port}"
        except OSError:
            continue
            
    return None

