async def _fetch_from_yandere_paginated(session, headers, base_tags, rating_tag, max_page, site_url="https://yande.re/post.json", proxy=None, negative_tags=None) -> Optional[str]:
    if max_page <= 0: return None
    site_name = "Yande" if "yande" in site_url else "Kona"
    async def request_with_fallback(url, params):
        try:
            async with session.get(url, params=params, headers=headers, proxy=proxy) as resp:
                if resp.status != 200: raise aiohttp.ClientConnectionError("Bad status")
                return await resp.json()
        except (aiohttp.ClientError, asyncio.TimeoutError, OSError):
            if proxy:
                print(f"    ⚠️ [{site_name}] Сбой прокси. Пробую DIRECT...")
                async with session.get(url, params=params, headers=headers, proxy=None) as resp:
                    if resp.status != 200: return None
                    return await resp.json()
            return None

    for _ in range(3): # 3 попытки разных страниц
        try:
            page = random.randint(1, max_page)
            config_key = "yande.re" if "yande" in site_url else "konachan_com"
            neg = " ".join(_merge_negative_tags(
                BOORU_API_CONFIGS.get(config_key, {}).get("negative_tags", []),
                negative_tags,
            ))
            tags = f'{base_tags} {rating_tag} {neg}'.strip()
            params = {'tags': tags, 'limit': 100, 'page': page}

            query_hash = hashlib.sha256(tags.encode("utf-8", "ignore")).hexdigest()[:10]
            print(f"    🔎 [{site_name}] p.{page} rating={rating_tag} qhash={query_hash}")
            
            posts = await request_with_fallback(site_url, params)
            
            if not posts or not isinstance(posts, list):
                continue

            # Фильтруем пустые URL
            valid_posts = [p for p in posts if p.get('file_url') and not _post_has_blocked_tags(p)]
            if not valid_posts: continue

            img = random.choice(valid_posts).get('file_url')
            if img and img.startswith("//"): img = "https:" + img
            return img

        except Exception:
            continue
            
    return None

async def _fetch_from_booru_api(session, api_type, base_tags, rating_tag, headers, proxy=None, apply_negative_tags=True) -> Optional[str]:
    config = BOORU_API_CONFIGS.get(api_type)
    if not config: return None

    # Внутренняя функция для безопасного GET запроса
    async def safe_get(url, params):
        try:
            async with session.get(url, params=params, headers=headers, proxy=proxy) as resp:
                if resp.status != 200: return None
                return await resp.read() # Читаем байты
        except (aiohttp.ClientError, asyncio.TimeoutError, OSError):
            if proxy:
                print(f"    ⚠️ [{api_type}] Сбой прокси. Пробую DIRECT...")
                async with session.get(url, params=params, headers=headers, proxy=None) as resp:
                    if resp.status != 200: return None
                    return await resp.read()
            return None

    try:
        main_params = config['params'].copy()
        tags_query = f'{base_tags} {rating_tag}'
        neg = " ".join(_merge_negative_tags(config.get('negative_tags', []))) if apply_negative_tags else ""
        final_tags = f'{tags_query} {neg}'.strip()
        
        if api_type == 'gelbooru':
            main_params.update({'tags': f'{final_tags} sort:random'.strip(), 'api_key': config.get('key'), 'user_id': config.get('user')})
        elif api_type == 'danbooru':
            main_params.update({'tags': final_tags, 'random': 'true', 'login': config.get('user'), 'api_key': config.get('key')})
        elif api_type == 'aibooru':
            main_params.update({'tags': final_tags, 'random': 'true'})

        # Чистим параметры
        main_params = {k: v for k, v in main_params.items() if v is not None}

        body = await safe_get(config['url'], main_params)
        if not body: return None

        data = json.loads(body)
        
        # Разбор ответа
        posts = []
        if api_type == 'gelbooru': posts = data.get('post', [])
        elif isinstance(data, list): posts = data
        
        if not posts: return None
        
        valid_posts = [post for post in posts if not _post_has_blocked_tags(post)]
        if not valid_posts: return None

        post = random.choice(valid_posts)
        url = post.get('file_url') or post.get('large_file_url')
        
        if url:
            if url.startswith("//"): url = "https:" + url
            if api_type == 'danbooru' and not url.startswith("http"):
                url = f"https://danbooru.donmai.us{url}"
            return url

    except Exception:
        pass
        
    return None

async def _process_fallback_api_response(api_source: str, response: aiohttp.ClientResponse) -> Optional[str]:
    # Исправление синтаксической ошибки и улучшение логики парсинга ---
    try:
        data = await response.json()
        if not isinstance(data, dict):
             print(f"[{api_source}] API Info: Ответ не является JSON-объектом.")
             return None
    except (json.JSONDecodeError, aiohttp.ContentTypeError) as e:
        print(f"⛔ [{api_source}] КРИТИЧЕСКАЯ ОШИБКА РАЗБОРА JSON: {type(e).__name__}: {e}")
        return None

    url = None
    # Явная и строгая проверка для конкретного источника API
    if api_source == "nekos.best":
        results = data.get('results')
        # Дополнительные проверки на то, что results - это непустой список
        if results and isinstance(results, list) and len(results) > 0 and isinstance(results[0], dict):
            url = results[0].get('url')
    else:
        # Логика по умолчанию для всех остальных API (например, waifu.pics)
        url = data.get('url')

    # Финальная проверка, что URL был найден и является строкой
    if not url or not isinstance(url, str):
        print(f"[{api_source}] API Info: Ключ 'url' не найден в ответе JSON или имеет неверный тип.")
        return None

    return url
