async def _get_proxy_usage_strategy() -> bool:
    """Определяет, следует ли использовать прокси, на основе кэшированного состояния."""
    async with PROXY_STATE_LOCK:
        if not PROXY_STATE['is_working']:
            if time.time() - PROXY_STATE_LAST_FAILURE > PROXY_STATE_COOLDOWN:
                print("Прокси-кулдаун истек. Повторно пробую использовать прокси...")
                PROXY_STATE['is_working'] = True
        return PROXY_STATE['is_working']

async def _update_proxy_state_on_failure():
    """Обновляет глобальное состояние при сбое прокси. ИСПРАВЛЕНАЯ ВЕРСИЯ."""
    global PROXY_STATE_LAST_FAILURE # <--- ЭТО ИСПРАВЛЕНИЕ
    async with PROXY_STATE_LOCK:
        # Проверяем еще раз, чтобы избежать лишних сообщений в логе от параллельных задач
        if PROXY_STATE['is_working']:
            print("ОШИБКА ПРОКСИ: Глобально отключаю использование прокси на 60 секунд.")
            PROXY_STATE['is_working'] = False
            PROXY_STATE_LAST_FAILURE = time.time() # Теперь обновляется глобальная переменная

