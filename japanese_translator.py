async def fetch_with_fallback(session, url, headers, params=None, proxy=None, source="api"):
    """Вспомогательная функция: пробует прокси, если ошибка -> пробует напрямую."""
    try:
        async with session.get(url, headers=headers, params=params, proxy=proxy) as response:
            return response, await response.read() # Читаем сразу, чтобы не потерять данные
    except (aiohttp.ClientConnectorError, aiohttp.ServerDisconnectedError, asyncio.TimeoutError, OSError):
        if proxy:
            # Если прокси не сработал, пробуем НАПРЯМУЮ (вдруг включен OpenVPN)
            print(f"    ⚠️ [{source}] Прокси сбой. Пробую DIRECT...")
            async with session.get(url, headers=headers, params=params, proxy=None) as response:
                return response, await response.read()
        raise # Если прокси не было, а ошибка есть — пробрасываем

