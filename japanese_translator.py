def _merge_negative_tags(*groups):
    merged = []
    seen = set()
    for group in groups:
        for tag in group or []:
            value = str(tag).strip()
            if not value:
                continue
            key = value.lower().replace(" ", "_")
            if key not in seen:
                seen.add(key)
                merged.append(value)
    return merged

