# Создаем единое регулярное выражение для всех слов, которые нужно заменить.
# Сортируем ключи по длине в обратном порядке, чтобы избежать частичных совпадений.
_sorted_keys = sorted(GOPNIK_REPLACEMENTS.keys(), key=len, reverse=True)
_pattern = r'\b(' + '|'.join(re.escape(key) for key in _sorted_keys) + r')\b'
_gopnik_regex = re.compile(_pattern, flags=re.IGNORECASE)

def _gopnik_replacer(match: re.Match) -> str:
    original_word = match.group(0)
    replacement_options = GOPNIK_REPLACEMENTS.get(original_word.lower())
    
    if not replacement_options: 
        return original_word # Защита от непредвиденных сбоев Regex
        
    chosen = random.choice(replacement_options) if isinstance(replacement_options, list) else replacement_options

    if original_word.isupper() and len(original_word) > 1: # Полный капс
        return chosen.upper()
    elif original_word and original_word[0].isupper(): # Первая буква заглавная (Начало предложения)
        return chosen.capitalize()
    return chosen

