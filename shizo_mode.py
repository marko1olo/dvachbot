def _replacement_callback(match: re.Match) -> str:
    word = match.group(0).lower()
    options = SCHIZO_REPLACEMENTS.get(word)
    if options:
        return random.choice(options)
    return match.group(0)


def _stage_replace_words(text: str) -> str:
    return _REPLACEMENT_PATTERN.sub(_replacement_callback, text)

def _apply_bipolar_casing(text: str) -> str:
    """Биполярный регистр (заборчик)"""
    if random.random() < 0.08:
        return "".join(c.upper() if i % 2 == 0 else c.lower() for i, c in enumerate(text))
    return text

def _apply_mirror_words(text: str) -> str:
    """Зеркальное отражение длинных слов (читают задом наперед)"""
    if random.random() >= 0.12: 
        return text
    
    def _mirror(m):
        word = m.group(0)
        return word[::-1] if random.random() < 0.25 else word
        
    return re.sub(r'\b[А-Яа-яЁё]{5,}\b', _mirror, text)

def _apply_redacted(text: str) -> str:
    """Паранойя: цензурирование слов"""
    if random.random() >= 0.15:
        return text
        
    def _redact(m):
        return "██████" if random.random() < 0.15 else m.group(0)
        
    return re.sub(r'\b[А-Яа-яЁё]{4,}\b', _redact, text)

def _stage_glitch(text: str) -> str:
    if random.random() >= 0.15:
        return text

    chars = list(text)
    glitch_map = {
        'о': 'ооо', 'а': 'ааа', 'е': 'еее', 'и': 'ииии',
        'у': 'ууу', 'ы': 'ыыы', 'э': 'эээ',
    }
    indices = [i for i, c in enumerate(chars) if c.lower() in glitch_map]
    if not indices:
        return text

    num_glitches = max(1, len(indices) // 5)
    for idx in random.sample(indices, min(num_glitches, len(indices))):
        original = chars[idx]
        replacement = glitch_map.get(original.lower(), original)
        if original.isupper():
            replacement = replacement.upper()
        chars[idx] = replacement

    return ''.join(chars)


def _stage_hallucinations(text: str) -> str:
    if random.random() >= 0.20:
        return text

    words = text.split()
    if len(words) < 2:
        return text + " " + random.choice(HALLUCINATION_PHRASES)

    injection_point = random.randint(1, len(words) - 1)
    words.insert(injection_point, random.choice(HALLUCINATION_PHRASES))
    return ' '.join(words)


def _stage_numerology(text: str) -> str:
    match = _DIGIT_PATTERN.search(text)
    if not match:
        return text

    number = match.group(0)
    interp_key = number if number in NUMEROLOGY_INTERPRETATIONS else "*"
    interpretations = NUMEROLOGY_INTERPRETATIONS[interp_key]
    interpretation = random.choice(interpretations)

    if interp_key == "*":
        try:
            digit_sum = sum(int(d) for d in number)
            interpretation = f"СУММА ЦИФР = {digit_sum}. ЭТО ЧТО-ТО ЗНАЧИТ."
        except ValueError:
            pass

    return text.replace(number, f"{number} ({interpretation})", 1)


def _apply_random_caps(text: str) -> str:
    words = text.split()
    result = []
    for w in words:
        if random.random() < 0.35:
            result.append(w.upper())
        else:
            result.append(w)
    return ' '.join(result)


def _apply_vowel_stretch(text: str) -> str:
    if random.random() >= 0.15:
        return text

    chars = list(text)
    vowel_indices = [i for i, c in enumerate(chars) if c in _VOWELS_CYR]
    if not vowel_indices:
        return text

    idx = random.choice(vowel_indices)
    chars[idx] = chars[idx] * random.randint(2, 4)
    return ''.join(chars)


