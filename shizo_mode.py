def shizo_transform(text: str, header: Optional[str] = None) -> Tuple[str, ...]:
    if not text:
        return ('text', "")

    # Stage 0: критическая замена (5%)
    if random.random() < 0.05:
        return ('text', random.choice(CRITICAL_OVERRIDE_PHRASES))

    # Stage 0.5: визуальная генерация (25% при коротком тексте)
    if len(text) < 180 and random.random() < 0.25:
        # Теперь используем реальный хедер или дефолтный, если его нет
        display_header = header or "ШИЗО-ТРАНСЛЯЦИЯ"
        image_bytes = _try_create_visual(text=text, header=display_header)
        if image_bytes:
            return ('image', image_bytes)

    # === PIPELINE РАСПАДА СОЗНАНИЯ ===

    # Stage 1: Замена слов
    result = _stage_replace_words(text)

    # Stage 2: Глитч-эффект (15%)
    result = _stage_glitch(result)

    # Stage 3: Инъекции галлюцинаций (20%)
    result = _stage_hallucinations(result)

    # Stage 4: Нумерология
    result = _stage_numerology(result)

    # === ДОПОЛНИТЕЛЬНЫЕ СЛОИ ИСКАЖЕНИЯ ===

    result = _apply_word_salad(result)
    result = _apply_neologisms(result)
    result = _apply_bipolar_casing(result)
    result = _apply_mirror_words(result)
    result = _apply_redacted(result)
    result = _apply_acronym_expansion(result)
    result = _apply_perseveration(result)
    result = _apply_random_caps(result)
    result = _apply_vowel_stretch(result)
    result = _apply_parasite_injection(result)
    result = _apply_prefix(result)
    result = _apply_suffix(result)
    result = _apply_punctuation_chaos(result)
    result = _apply_schizo_numerology(result)

    return ('text', result)