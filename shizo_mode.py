def _apply_punctuation_chaos(text: str) -> str:
    if random.random() < 0.30:
        text = _PUNCTUATION_PATTERN.sub('', text)
    elif random.random() < 0.35:
        text = text.replace('.', '..', 1).replace(',', '... ', 1).replace('?', '?!', 1)
    return text


