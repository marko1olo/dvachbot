def load_roulette_data(file_path: str) -> List[Dict[str, Any]]:
    """
    Загружает данные из JSON-файла с рулетками и объединяет все события 
    из всех рулеток в один плоский список.

    Args:
        file_path: Путь к JSON-файлу.

    Returns:
        Список словарей, где каждый словарь представляет одно событие.
        Возвращает пустой список в случае ошибки.
    """
    all_events: List[Dict[str, Any]] = []
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            data = json.load(f)
            
        if not isinstance(data, dict):
            return []

        for roulette in data.get("roulettes", []):
            if not isinstance(roulette, dict):
                continue
            roulette_name = roulette.get("name", "Неизвестная рулетка")

            events = roulette.get("events", [])
            if not isinstance(events, list):
                continue

            for event in events:
                if not isinstance(event, dict):
                    continue
                # Добавляем название рулетки к каждому событию для контекста
                event_copy = event.copy()
                event_copy['source_roulette'] = roulette_name
                all_events.append(event_copy)
                
    except FileNotFoundError:
        print(f"⛔ КРИТИЧЕСКАЯ ОШИБКА: Файл рулетки не найден по пути: {file_path}")
        return []
    except json.JSONDecodeError:
        print(f"⛔ КРИТИЧЕСКАЯ ОШИБКА: Не удалось декодировать JSON из файла: {file_path}")
        return []
        
    return all_events

def get_random_event(all_events: List[Dict[str, Any]]) -> Optional[Dict[str, Any]]:
    """
    Выбирает случайное событие из предоставленного списка всех событий.

    Args:
        all_events: Плоский список всех событий из всех рулеток.

    Returns:
        Словарь со случайным событием или None, если список пуст.
    """
    if not all_events:
        return None
    return random.choice(all_events)