# 1. Создаем одно большое регулярное выражение из всех ключей словаря
# Сортируем ключи по длине в обратном порядке, чтобы длинные совпадения (напр. "права человека")
# обрабатывались раньше коротких (напр. "права").
_ideological_keys = sorted(IDEOLOGICAL_REPLACEMENTS.keys(), key=len, reverse=True)
_ideological_pattern = r'\b(' + '|'.join(re.escape(key) for key in _ideological_keys) + r')\b'
_ideological_regex = re.compile(_ideological_pattern, flags=re.IGNORECASE)

# Предкомпилированные регулярки для _apply_kancelarit
_ENGLISH_PATTERN = re.compile(r'\b[a-zA-Z]{2,}\b')
_CAPS_IMPORTANT_PATTERN = re.compile(r'\b[а-яА-ЯёЁ]{6,}\b')
_KANCELARIT_MAP_COMPILED = [
    (re.compile(pattern, flags=re.IGNORECASE), repl)
    for pattern, repl in [
        (r'\bя (сделал|сделала)\b', 'мною был реализован комплекс мер'),
        (r'\bмы (решили|подумали)\b', 'в ходе межведомственного согласования было принято решение'),
        (r'\b(хочу|хотим)\b', 'выражаем крайнюю озабоченность и заинтересованность'),
        (r'\b(будет|будут)\b', 'планируется к реализации в рамках импортозамещения'),
        (r'\bпроблем(а|ы|у)\b', 'определенные геополитические вызовы'),
        (r'\bошибка\b', 'отрицательный результат'),
        (r'\b(упал|упало|упали)\b', 'продемонстрировал отрицательный рост'),
        (r'\b(купил|купили)\b', 'произвел импортозамещение'),
    ]
]

# 2. Создаем функцию-заменитель, которая будет вызываться для каждого совпадения
def _zaputin_ideological_replacer(match: re.Match) -> str:
    """
    Находит замену для совпавшего слова в словаре IDEOLOGICAL_REPLACEMENTS,
    сохраняя при этом регистр оригинального слова.
    """
    original_word = match.group(0)
    # Ключ для словаря всегда в нижнем регистре
    key = original_word.lower()
    
    replacement_options = IDEOLOGICAL_REPLACEMENTS[key]
    chosen_replacement = random.choice(replacement_options)

    # Восстанавливаем регистр: если исходное слово было с большой буквы или полностью заглавное
    if original_word.isupper():
        return chosen_replacement.upper()
    elif original_word.istitle():
        return chosen_replacement.capitalize()
    else:
        return chosen_replacement
def _apply_kancelarit(text: str) -> str:
    # 1. Ловим ЛЮБЫЕ слова на латинице (английские) и клеймим их!
    # Игнорируем слова короче 2 букв, чтобы не трогать случайные символы
    def _quote_english(m):
        word = m.group(0)
        # Грубая проверка на множественное число в английском (на конце "s")
        is_plural = word.lower().endswith('s')
        
        prefixes =[
            "т.н.", 
            "так называем" + ("ые" if is_plural else "ый"), 
            "пресловут" + ("ые" if is_plural else "ый"), 
            "чужд" + ("ые" if is_plural else "ый") + " нам",
            "навязанн" + ("ые" if is_plural else "ый") + " англосаксами",
            "так называем" + ("ые" if is_plural else "ый") + " западн" + ("ые" if is_plural else "ый")
        ]
        prefix = random.choice(prefixes)
        return f'{prefix} «{word}»'

    # Регулярка ищет любые слова из латинских букв (длиной от 2 символов)
    text = _ENGLISH_PATTERN.sub(_quote_english, text)
    
    # 2. Сухой канцелярит: превращаем обычные слова в бюрократический ад чиновника
    for pattern, repl in _KANCELARIT_MAP_COMPILED:
        text = pattern.sub(repl, text)
    
    # 3. Интонационная КАПСЛОКОВИЗАЦИЯ ВАЖНЫХ слов (стиль Соловьёва)
    def _caps_important(m):
        word = m.group(0)
        # Капсим с шансом 15% длинные русские слова (от 6 букв)
        return word.upper() if random.random() < 0.15 else word
        
    text = _CAPS_IMPORTANT_PATTERN.sub(_caps_important, text)
    
    return text
    
def zaputin_transform(text: str) -> str:
    """
    Преобразует текст в стиль режима zaputin. (ВЕРСИЯ С ОПТИМИЗАЦИЕЙ)
    """
    if not text:
        return text

    # *** ЭФФЕКТИВНАЯ ЗАМЕНА: Один проход по тексту для всех идеологических замен ***
    transformed_text = _ideological_regex.sub(_zaputin_ideological_replacer, text)

    # Механика замены букв Z/V остается без изменений
    replacements = {
        'З': 'Z', 'з': 'Z',
        'В': 'V', 'в': 'V',
    }
    # Этот цикл можно оставить, так как .replace() очень быстр для простых замен символов,
    # и здесь их всего несколько.
    for old, new in replacements.items():
        transformed_text = transformed_text.replace(old, new)
    transformed_text = _apply_kancelarit(transformed_text)

    # Вероятностное добавление лозунга
    if len(transformed_text.split()) > 3 and random.random() < 0.25:
        slogan = random.choice(PATRIOTIC_PHRASES)
        transformed_text += f"\n\n<b>{slogan}</b>"
    
    return transformed_text